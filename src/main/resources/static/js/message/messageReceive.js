const receiveMessage = document.querySelectorAll(".receiveMessage");

receiveMessage.forEach(r => {
    r.addEventListener("click", ()=>{
        const p = new URLSearchParams();
        let messageNum = r.getAttribute("data-message-num");
        p.append("messageNum", messageNum);

        fetch("/message/read",{
            method: "POST",
            body: p
        })
        
    })
})